    private void btnCreateAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateAccountActionPerformed
        //Creates account
        String username = txtInputUsername.getText().trim();
        String password = new String(txtInputPassword.getPassword()).trim();

        //Checks if username and password input is empty
        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Please fill all fields.",
                    "Missing Fields",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        //Checks if username is less than 4 character
        if (username.length() < 4) {
            JOptionPane.showMessageDialog(this,
                    "Username must be at least 4 characters.",
                    "Invalid Username",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        //Checks if password is less than 6 character
        if (password.length() < 6) {
            JOptionPane.showMessageDialog(this,
                    "Password must be at least 6 characters.",
                    "Weak Password",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        //Checks if a gender is selected or not
        if (!rbtnMale.isSelected() && !rbtnFemale.isSelected()) {
            JOptionPane.showMessageDialog(this,
                    "Please select a gender.",
                    "Missing Gender",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        registerUser(username, password);

    }//GEN-LAST:event_btnCreateAccountActionPerformed

