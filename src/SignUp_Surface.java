    private void registerUser(String username, String password) {
        //Database registration
        String checkSQL = "SELECT id FROM users WHERE username = ?";
        String insertSQL = "INSERT INTO users (username, password, role, gender, birthday) VALUES (?, ?, 'user', ?, ?)";
        Connection connect = DatabaseConnection.getConnection();

        if (connect == null) {
            JOptionPane.showMessageDialog(this,
                    "Cannot connect to database.",
                    "Database Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            //Checks if username is already taken or not
            PreparedStatement checks = connect.prepareStatement(checkSQL);
            checks.setString(1, username);

            ResultSet setResult = checks.executeQuery();

            if (setResult.next()) {
                JOptionPane.showMessageDialog(this,
                        "Username \"" + username + "\"is already taken. \nPlease choose a different one.",
                        "Username Taken",
                        JOptionPane.WARNING_MESSAGE);
                return;
            }

            //Identifies the gender selected
            String gender = "Not set";
            if (rbtnMale.isSelected()) {
                gender = "Male";
            } else if (rbtnFemale.isSelected()) {
                gender = "Female";
            }

            //Gets the user's birthday
            java.util.Date selectedDate = (java.util.Date) spnSelectBirthday.getValue();
            java.text.SimpleDateFormat dateFormat = new java.text.SimpleDateFormat("MM-dd-yyyy");
            String birthday = dateFormat.format(selectedDate);

            //Inserts a new user with 'user' role
            PreparedStatement inserts = connect.prepareStatement(insertSQL);
            inserts.setString(1, username);
            inserts.setString(2, password);
            inserts.setString(3, gender);
            inserts.setString(4, birthday);
            inserts.executeUpdate();

            JOptionPane.showMessageDialog(this,
                    "Account created successfully! \nYou can now log in.",
                    "Sign Up Successful",
                    JOptionPane.INFORMATION_MESSAGE);

            //Return to LogIn_Surface after successfully register an account
            dispose();
            new LogIn_Surface().setVisible(true);

        } catch (SQLException e) {
            logger.severe("Sign-up error: " + e.getMessage());
            JOptionPane.showMessageDialog(this,
                    "Database Error: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }

    }

